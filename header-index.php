/*
 *---------------------------------------------------------------
 * APPLICATION ENVIRONMENT
 *---------------------------------------------------------------
 *
 * You can load different configurations depending on your
 * current environment. Setting the environment also influences
 * things like logging and error reporting.
 *
 * This can be set to anything, but default usage is:
 *
 *     development
 *     testing
 *     production
 *
 * NOTE: If you change these, also change the error_reporting() code below
 */
	// Redirect to Portal
	if ($_SERVER['REQUEST_URI'] == '/' || $_SERVER['REQUEST_URI'] == '/index.php') {
		http_response_code(301);
		header("Location: /portal");
		die();
	}

	define('ENVIRONMENT', isset($_SERVER['CI_ENV']) ? $_SERVER['CI_ENV'] : 'development');
	
	date_default_timezone_set('Asia/Jakarta');
	
/*
 *---------------------------------------------------------------
 * GLOBAL BASE SITE URL 
 *---------------------------------------------------------------
 *
 * Nilai ada disini akan ditambahkan ke dalam nilai $config['base_url']
 * TANPA garis miring di akhir
 */
	if (ENVIRONMENT == 'production')
		if (isset($_SERVER['HTTPS']))
			define('GLOBAL_BASE_URL', 'https://' . $_SERVER['HTTP_HOST']);
		else
			define('GLOBAL_BASE_URL', 'http://' . $_SERVER['HTTP_HOST']);
	if (ENVIRONMENT == 'development')
		if (php_sapi_name() !== 'cli')
			define('GLOBAL_BASE_URL', 'http://' . $_SERVER['HTTP_HOST']);
		else
			define('GLOBAL_BASE_URL', 'http://localhost/');  // asumsi pakai localhost jika cli agar tak error

	// Assign to config
	$assign_to_config['global_base_url'] = GLOBAL_BASE_URL;
	
/*
 *---------------------------------------------------------------
 * ERROR REPORTING
 *---------------------------------------------------------------
 *
 * Different environments will require different levels of error reporting.
 * By default development will show errors but testing and live will hide them.
 */
switch (ENVIRONMENT)
{
	case 'development':
		error_reporting(E_ALL);
		ini_set('display_errors', 1);
	break;

	case 'testing':
	case 'production':
		ini_set('display_errors', 0);
		if (version_compare(PHP_VERSION, '5.3', '>='))
		{
			error_reporting(E_ALL & ~E_NOTICE & ~E_DEPRECATED & ~E_STRICT & ~E_USER_NOTICE & ~E_USER_DEPRECATED);
		}
		else
		{
			error_reporting(E_ALL & ~E_NOTICE & ~E_STRICT & ~E_USER_NOTICE);
		}
	break;

	default:
		header('HTTP/1.1 503 Service Unavailable.', TRUE, 503);
		echo 'The application environment is not set correctly.';
		exit(1); // EXIT_ERROR
}

